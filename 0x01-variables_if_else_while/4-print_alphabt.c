#include <stdio.h>
#include <stdlib.h>
#include <time.h>
/**
 * main - print if the number if positive, zero, or negative
 * this program prints "Programming is positive, zero, or negative
 * Return: 0
 */
int main(void)
{
	char c;

	c = 'a';
	while
		(c <= 'z') {
			if (( c != 'q' && c != 'e') && c <= 'z')
				putchar(c);
			c++;
		}
	putchar('\n');
	return (0);
}
